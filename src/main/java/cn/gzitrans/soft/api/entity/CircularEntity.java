package cn.gzitrans.soft.api.entity;

public class CircularEntity {
	
	private Integer x;//区域圆的x轴坐标
	
	private Integer y;//区域圆的y轴坐标
	
	private Integer radius;//区域圆的半径

	public Integer getX() {
		return x;
	}

	public void setX(Integer x) {
		this.x = x;
	}

	public Integer getY() {
		return y;
	}

	public void setY(Integer y) {
		this.y = y;
	}

	public Integer getRadius() {
		return radius;
	}

	public void setRadius(Integer radius) {
		this.radius = radius;
	}
	
	

}
